			$.ajaxSetup({
                headers: {

                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });

			$('.add-to-cart').click(function(e){
				e.preventDefault();
				var checkLogin = "{{Auth::Check()}}";
                if (checkLogin) {
                    var id =  $(this).closest(".overlay-content").find('h1').text();
                $.ajax({
                    type:'POST',
                    url:'{{url("users/product/add-to-cart")}}',
                    data:{
                        qty: 1,
                        product_id: id,
                    },
                    success:function(data){
						// console.log(data);
                        $('div.shop-menu ul li:nth-child(4) a sup').html(data);
                    }
                })
                } else {
                    alert ('Chua Login')
                }
			})
		})
